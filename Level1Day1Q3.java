//Java Program to print Multiplication of two floating point Number.

class Level1Day1Q3 {

    public static void main(String[] args) {

        // f is to ensures that numbers are float DATA TYPE
        float f1 = 1.5f;
        float f2 = 2.0f;

        float p = f1 * f2;

        System.out.println("Product is: " + p);
    }
}
